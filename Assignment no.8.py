#  Write a program to check whether given number is positive or negative

# /***************************************************** SOLUTION **************************************************************************/


num = float(input("Enter a number: "))
if num > 0:
   print("Positive number")
elif num == 0:
   print("Zero")
else:
   print("Negative number")
   
